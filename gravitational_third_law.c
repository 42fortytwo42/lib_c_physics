/*
**	This algorithm implement gravitational capabilities : Newton Third Law
**
**	Return : (0) if float force is null.
**	(float) force is returned. So simple...
**
**	Thanks to Universite Caen Basse-Normandie (Biologie) to my physic lessons.
**	Thomas LE MIGNAN (2014 / Paris - France).
*/

float	ft_gravitational_third_law(float force)
{
	if (force == 0)
		return (0);
	return (force);
}
